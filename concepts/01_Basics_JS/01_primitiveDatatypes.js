/* 
Primitive Data Types: 
1)	Stored directly in the location.
2)	Stored in the stack.
*/

// ==> Number
const int = 123;
const float = 12.3;

// ==> String
const str = "Hello";

// ==> boolean
const boo = true;

// ==> null
const value1 = null;

// ==> undefined
const value2 = undefined;

console.log(int);
console.log(float);
console.log(str);
console.log(boo);
console.log(value1);
console.log(value2);
